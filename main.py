def on_on_overlap(sprite, otherSprite):
    if info.score() > 50:
        game.game_over(True)
        game.set_game_over_scoring_type(game.ScoringType.HIGH_SCORE)
    else:
        game.game_over(False)
        game.set_game_over_scoring_type(game.ScoringType.HIGH_SCORE)
sprites.on_overlap(SpriteKind.enemy, SpriteKind.player, on_on_overlap)

